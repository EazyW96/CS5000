public class CheckPoint2_7_2 {
    public static void main(String[] args) {
        // Step 1: Declare a double variable named miles with initial value 100
        double miles = 100;
        
        // Step 2: Declare double constant named KILOMETERS_PER_MILE with value 1.609
        final double KILOMETERS_PER_MILE = 1.609;
        
        // Step 3: Delcare a double variable name kilometers, multiply miles and KILOMETERS_PER_MILE, assign result to kilometers
        double kilometers = miles * KILOMETERS_PER_MILE;
        
        // Step 4: Display kilometers to the console
        System.out.println(" The value of kilometers is  " + kilometers);
    }
}
